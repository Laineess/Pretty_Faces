export default function Sidebar({ open, onClose }) {
  const { user, logout } = useAuth()
  const navigate = useNavigate()

  function handleLogout() {
    logout()
    navigate('/admin/login')
  }

  const visible = user?.rol === 'empleada'
    ? empleadaNavItems
    : navItems.filter(item => !item.adminOnly || user?.rol === 'admin')

  return (
    <>
      {/* Backdrop — mobile only */}
      <div
        className={`fixed inset-0 bg-black/50 z-40 md:hidden transition-opacity duration-300 ${
          open ? 'opacity-100 pointer-events-auto' : 'opacity-0 pointer-events-none'
        }`}
        onClick={onClose}
      />

      <aside className={`
        fixed inset-y-0 left-0 z-50 w-64 bg-gray-900 flex flex-col
        transform transition-transform duration-300 ease-in-out
        md:static md:z-auto md:translate-x-0 md:sticky md:top-0 md:h-screen md:shrink-0
        ${open ? 'translate-x-0' : '-translate-x-full'}
      `}>
        {/* Logo */}
        <div className="px-6 py-6 border-b border-white/10">
          <img
            src="/images/Logo.jpg"
            alt="Pretty Face's"
            className="h-12 w-auto object-contain brightness-0 invert"
          />
          <p className="font-sans text-white/40 text-xs mt-2 tracking-widest uppercase">
            Panel de Control
          </p>
        </div>

        {/* Nav */}
        <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
          {visible.map(item => (
            <NavLink
              key={item.href}
              to={item.href}
              onClick={() => onClose?.()}
              className={({ isActive }) =>
                `flex items-center gap-3 px-4 py-2.5 rounded-xl font-sans text-sm font-medium transition-all duration-200 ${
                  isActive
                    ? 'bg-gold text-white shadow-md'
                    : 'text-white/60 hover:text-white hover:bg-white/10'
                }`
              }
            >
              {item.icon}
              {item.label}
            </NavLink>
          ))}
        </nav>

        {/* User + logout */}
        <div className="px-4 py-4 border-t border-white/10">
          <div className="flex items-center gap-3 px-2 mb-3">
            <div className="w-8 h-8 rounded-full bg-gold flex items-center justify-center text-white text-xs font-bold">
              {user?.nombre?.charAt(0).toUpperCase()}
            </div>
            <div>
              <p className="font-sans text-white text-xs font-medium">{user?.nombre}</p>
              <p className="font-sans text-white/40 text-xs capitalize">{user?.rol}</p>
            </div>
          </div>
          <button
            onClick={handleLogout}
            className="w-full flex items-center gap-3 px-4 py-2.5 rounded-xl font-sans text-sm text-white/50 hover:text-white hover:bg-red-500/20 transition-all duration-200"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={2} viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Cerrar Sesión
          </button>
        </div>
      </aside>
    </>
  )
}
